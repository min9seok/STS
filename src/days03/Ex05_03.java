public class Ex05_03 {
	public static void main(String[] args) {
		
		System.out.println(10 > 3 ); //true
		System.out.println(10 < 3 ); //false
		System.out.println(10 >= 3 ); //true
		System.out.println(10 <= 3 ); //false
		
		//주의
		System.out.println(10 == 3 ); //false
		System.out.println(10 != 3 ); //true
		
		
	}//main
}//class
